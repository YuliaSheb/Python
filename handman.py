lists = []
used = []
for i in range(0, word_len):
    lists.append("_")

t = False
while not t:
    letter = input("What letter?\n").lower()
    if letter in used:
        print(f"You've already guessed {letter}")
    else:
        n = 0
        for position in range(word_len):
            letters = word[position]
            if letters == letter:
                lists[position] = letter
                used.append(letter)
        if letter not in word:
            used.append(letter)
            if lives == 0:
                print(stages[0])
                print("You lose")
                print(f"Word is {word}")
                t = True
            else:
                print(f"You guessed {letter}, that's not in the word. You lose a life")
                lives -= 1
            n += 1
    print(stages[lives])
    print(f"{' '.join(lists)}")

    if "_" not in lists:
        t = True
        print("You win.")
